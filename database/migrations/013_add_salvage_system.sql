-- Add salvage items database table
CREATE TABLE IF NOT EXISTS salvage_items (
    id SERIAL PRIMARY KEY,
    tier VARCHAR(10) NOT NULL,
    roll_min INTEGER NOT NULL,
    roll_max INTEGER NOT NULL,
    item_name TEXT NOT NULL,
    value INTEGER NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_salvage_tier ON salvage_items(tier);
CREATE INDEX idx_salvage_roll ON salvage_items(tier, roll_min, roll_max);

